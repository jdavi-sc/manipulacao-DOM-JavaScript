function estilizar() {
    document.getElementById("style").style.fontFamily = 'verdana';
    document.getElementById("style").style.fontSize = '15px';
    document.getElementById("style").style.color = 'White';
    document.getElementById("style").style.backgroundColor = 'black';
    document.getElementById("style").style.textTransform = 'uppercase';
    document.getElementById("style").innerHTML = ('Praticando manipulação do DOM com JavaScript!!!')
}